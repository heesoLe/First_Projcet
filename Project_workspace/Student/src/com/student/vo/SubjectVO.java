package com.student.vo;

public class SubjectVO {
	
	private String subject_name;
	private String subject_teacher;
	private String subject_room;
	private String subject_date;
	private String subject_time;
	private String subject_status;
	
	public String getSubject_name() {
		return subject_name;
	}
	public void setSubject_name(String subject_name) {
		this.subject_name = subject_name;
	}
	public String getSubject_teacher() {
		return subject_teacher;
	}
	public void setSubject_teacher(String subject_teacher) {
		this.subject_teacher = subject_teacher;
	}
	public String getSubject_room() {
		return subject_room;
	}
	public void setSubject_room(String subject_room) {
		this.subject_room = subject_room;
	}
	public String getSubject_date() {
		return subject_date;
	}
	public void setSubject_date(String subject_date) {
		this.subject_date = subject_date;
	}
	public String getSubject_time() {
		return subject_time;
	}
	public void setSubject_time(String subject_time) {
		this.subject_time = subject_time;
	}
	public String getSubject_status() {
		return subject_status;
	}
	public void setSubject_status(String subject_status) {
		this.subject_status = subject_status;
	}

}
